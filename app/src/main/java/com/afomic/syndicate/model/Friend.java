package com.afomic.syndicate.model;

public class Friend {
    private String id;
    private String userID;
    public Friend(){

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }
}
